	<div class="media-body">
			<p> 発音を調べる

ロンドンレビューで「2012年に注目すべきアーティスト」とラベル付けされたジョナサンは、美術史の学生に支払われた史上最高額のコミッションをすでに販売しています。オイルとミクストメディアの作品であるGratitude Resortというタイトルの作品は75万ドルで販売され、ジョナサンはすべての収益を発展途上国の創造的な子供たちにカレッジアート奨学金を提供するArt for Peaceに寄付しました。</p>
			<p> 発音を調べる

ジョナサンの非営利セクターへの関心は、芸術に基づいた募金活動や芸術販売を通じて、他のさまざまな組織の資金調達に役立っています。彼は非営利団体で「フィルアートトロピスト」として知られるようになりました。このタイトルは、尋ねられたときにジョナサンから羊のような笑みを浮かべます。ジョナサンはロンドンビジネススクールで非営利経営を学び、2つの好きな世界（芸術と慈善活動）でニッチを見つけたことを嬉しく思います</p>
