	<h2>バロット・ベリンガム</h2>
	<img class="pull-left img-rounded" src="images/artists/Barot_Bellingham_tn.jpg" alt="Photo of Barot Bellingham">
	<div class="media-body">
		<p>バロットは、母親が冷蔵庫に最初のクレヨン絵を描いたとき、熟した2歳の芸術家になるという彼の決定をたどることができます。その時点から、彼は芸術に夢中になり、5歳という早い時期に成熟した芸術作品を作り始めました。子供の天才、バロットは2000歳で9歳で最初のプロの絵画を売り、11歳になるまでに美術の講義をしていた。</p>
		<p> 発音を調べる

バロットは、絵画と彫刻の王立アカデミーで最後の年を終えたばかりで、そこではガラスエッチングの絵画と肖像画に秀でていました。彼の世代で最も多様なアーティストの一人として称賛されたバロットは、油絵具と同じように水彩画にも熟練しており、さまざまな分野でバランスがとれています。 「The Un-Collection」と題されたバロットのコレクションは、ギルバート・ホールの壁を飾り、彼のスキルと感性の範囲を描写します。</p>
