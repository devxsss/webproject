	<h2>ヒラリー・ゴールドウィン・ポスト</h2>
	<img class="pull-left img-rounded" src="images/artists/Hillary_Goldwynn_tn.jpg" alt="Photo of Hillary Goldwynn">
	<div class="media-body">
		<p> 発音を調べる

ヒラリーは、ニューヨーク大学の2年生の美術彫刻の学生であり、神性サークル、国際画家賞、パリアカデミー賞など、新しい画家の主要な国際賞をすでにすべて受賞しています。ヒラリーのCAC展示では、波、深海、川などの水の画像のみを含む絵画を展示しています。</p>
		<p>熱心なウォータースポーツの参加者であるヒラリーは、他の人が持っていない、またはチャンスがないかもしれないさまざまな方法で水を理解しています。 CAC展示の作成における彼女の目標は、世界中の自然の水の美しさ、力、流れを他の人と共有することでした。ディスプレイに加えて、ヒラリーは火曜日に深海の驚異と呼ばれるセッションも開催します。これは深海でのダイビングとシュノーケリングへの愛と、水中探検の美しさをキャンバスでキャプチャするための指導を組み合わせています。</p>
