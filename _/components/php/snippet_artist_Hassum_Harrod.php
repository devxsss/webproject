	<h2>ハッサム・ハロッド</h2>
	<img class="pull-left img-rounded" src="images/artists/Hassum_Harrod_tn.jpg" alt="Photo of Hassum Harrod">
	<div class="media-body">
		<p>ニューデリーのアートカレッジは、大学での学士課程全体の奨学金でHassumを後援しました。同時代の風景画に大きな期待が寄せられており、同じ部分は落ち着いた鮮やかな色調を使用し、芸術ではほとんど矛盾しています。祖父と父の足跡をたどって技術者になると期待されていたハッサムは、彼の心に従い、決して振り返ることはありませんでした。現在、ハッサムはサンフランシスコアートアカデミーでMFAを勉強しており、印象的な色彩理論と比類のないカラーパレットで知られるようになり、アンダーグラウンドの大ファンを獲得しています。</p>
		<p>睡眠中に彼に来る風景やシーンは、ハッサムの作品の多くに影響を与えました。実際、彼は夢からインスピレーションを得ているため、ベッドサイドでキャンバスと木炭を持ち、ベッドからのシーンの最初のアウトラインをスケッチする日を始めます-夢の鮮やかさが消え始める前に。ハッサムは木曜日の議題の中で「現代美術における色の使用と不在」について話します。</p>
