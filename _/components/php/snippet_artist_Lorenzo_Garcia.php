	<h2>ロレンツォ・ガルシア</h2>
	<img class="pull-left img-rounded" src="images/artists/Lorenzo_Garcia_tn.jpg" alt="Photo of Lorenzo Garcia">
	<div class="media-body">
		<p> 発音を調べる

ロレンツォはメキシコで生まれましたが、母親が1歳のときにロサンゼルスに移住した後、南カリフォルニアで育ちました。彼の母親はファッション地区で仕立て屋として働き、ロレンツォに初期のミクストメディアアートを作成するためのスクラップ材料を持ち帰りました。その時点から、ロレンツォはスクラップ金属、布地、木、キャンバス、および他の多くからアートを作成することに夢中になりました。ロサンゼルスのBischon Art Schoolでの3年生の間に、彼は独自の独自のキャンバスにセラミックon薬を塗る方法を完成させ、月曜日のセッション「Art in Full Bloom」でデモンストレーションします。</p>
		<p> 発音を調べる

ロレンツォは並外れた量の色で絵を描き、自然、動物、科学を中心としたアートを作成することを好みます。現在、ビションでの4年生で、ロレンソは古い電柱や他のリサイクル素材で作られた混合メディアトーテムポールを作成しており、今後のトレンドを刺激する可能性のある次の新しい技術をすでに計画しています。</p>
