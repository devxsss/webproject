	<h2>Xhou Ta</h2>
	<img class="pull-left img-rounded" src="images/artists/Xhou_Ta_tn.jpg" alt="Photo of Xhou Ta">
	<div class="media-body">
		<p>中国国際芸術大学の先輩であるXhouは、キャンバスに顕微鏡画像を背面投影することで表示される米粒のミニチュア彫刻や絵画で有名になりました。稲作農家の息子であるXhouは、学校が休校中に家族の農場で一生懸命働きました。彼は父親のように農民になりたくないことを知っていましたが、常に父親の規律と貿易への献身を尊重していました。米粒に絵を描くことは、父親に敬意を払う方法として始まりました。</p>
		<p> 発音を調べる

大学の新入生として、Xhouは微生物学に魅了され、間もなく砂利、ビーズ、米などの非常に小さなアイテムの塗装面を「拡大」するために顕微鏡と望遠レンズを使用する特別なツールと技術を開発しました。 Xhouは最近、樹脂、粘土、その他の粘着性のある素材で作られたサムネイルのサイズの小さな彫刻を作成するためにテクニックを拡張しました。 Xhouは、水曜日のセミナー「Beyond the Naked Eye」で、彼の信じられないほど詳細な芸術作品の背後にある芸術と科学について議論します。</p>
