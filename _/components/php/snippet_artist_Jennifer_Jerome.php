	<h2>Jェニファー・ジェローム</h2>
	<img class="pull-left img-rounded" src="images/artists/Jennifer_Jerome_tn.jpg" alt="Photo of Jennifer Jerome">
	<div class="media-body">
		<p> 発音を調べる

ジェニファーはUCLAの学部生として心理学を学びましたが、すぐに彼女の心が絵と絵にあることを発見しました。常に理想主義者であり、彼女は両方を行う方法を見つけることに決め、芸術療法の修士号を追求しました。ジェニファーは現在、発達障害者のカウンセラーとして働いており、若年成人をリハビリするために芸術療法を使用しています。彼女はそれが惑星での彼女の目的を果たしていると感じています。アートセラピーでのキャリアとは別に、ジェニファーは毎晩、週末に絵を描いたり絵を描いたりし、非常に短い時間で多くの批評家から賞賛された作品を集めてきました。</p>
		<p> 発音を調べる

ニューオーリンズ出身のジェニファーの作品の多くは、洪水と再建を描いた抽象的な画像を中心としており、洪水後の数年間でティーンエイジャーとして成長しました。ジェニファーの作品は、荒廃と悲惨な生活の悲しみにもかかわらず、忍耐強いコミュニティの希望と一体感を描いています。ジェニファーの展示、Floods of Hopeは、火曜日のWater in Artテーマセッションで議論されます.</p>
