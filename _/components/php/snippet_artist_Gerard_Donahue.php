	<h2>ジェラルド・ケント・ドナヒュー</h2>
	<img class="pull-left img-rounded" src="images/artists/Gerard_Donahue_tn.jpg" alt="Photo of Gerard Donahue">
	<div class="media-body">
		<p> 発音を調べる

アートはジェラールの遺伝子にあります。彼の父、マストン・ジェラルド・ドナヒューは、ワシントンDCの「時間の風車」を含む私たちの時代の最も有名な彫像のいくつかを作成した彫刻家でした。ジェラルドが13歳のときにジェラルドの父親が早すぎる死を迎えた後、ジェラルドは父親から彫刻の初期の教訓を学び、彼の人生の情熱として彫刻の研究を続け、部分的に父親の遺産を完成させました。部分的には、熟練した彫刻家としてのこの独自の運命を築きます。彼は現在、オーストリアのウィーンにあるデシュトン大学のレントン奨学生であり、来年のアーティストインレジデンスのコミットメントを完了しています。</p>
		<p>ジェラールは、天然のタール、貴重な宝石、氷などの非伝統的な彫刻素材を使用するという点で、彫刻家としてユニークな道を開いています。彼は氷の彫刻を作成しました。氷の彫刻は、ガラスの後ろに常設冷蔵下で、いくつかの美術館や個人の家で展示されています。ジェラルドは火曜日の会議スケジュールで陰謀の噴水で氷の彫刻のデモンストレーションを行います。</p>
